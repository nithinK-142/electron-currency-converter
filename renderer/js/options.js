const switchButton = document.getElementById("switch");


// Populate currency dropdowns with options
currencies.forEach(currency => {
  fromCurrency.add(new Option(currency, currency));
  toCurrency.add(new Option(currency, currency));
});


// default values
const defaults = {
  fromCurrency: 'USD',
  toCurrency: 'INR',
  amount: 100,
};

fromCurrency.value = defaults.fromCurrency;
toCurrency.value = defaults.toCurrency;
amount.value = defaults.amount;


// Function to switch selected currencies
const switchCurrency = () => {
  const tempFrom = fromCurrency.value;
  fromCurrency.value = toCurrency.value;
  toCurrency.value = tempFrom;
}

switchButton.addEventListener("click", switchCurrency);